void main() {
  List<String> fruits = ["Apple", "Mango", "Orange", "Banana", "PineApple"];
  List<String> names = ["Govind", "Mohit", "Ajay", "Dhiraj", "Bharat"];
  print(fruits.length);
  print(fruits.reversed);
  print(fruits.hashCode);
  print(names.runtimeType);
  print(fruits.first);
  print(fruits.firstOrNull);
  print(fruits.indexed);
  print(names.isEmpty);
  print(names.isNotEmpty);
  print(names.iterator);
  print(names.last);
  print(names.lastOrNull);
  print(names.nonNulls);
  print(names.single);
  print(names.singleOrNull);
}
